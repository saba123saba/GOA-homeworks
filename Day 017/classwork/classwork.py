def add(num1, num2):
    return num1 + num2

def subtract(num1, num2):
    return num1 - num2

def multifly(num1, num2):
    return num1 * num2

def divide(num1, num2):
    return num1 / num2

print(add(5, 5))
print(subtract(5, 5))
print(multifly(5, 5))
print(divide(5, 5))